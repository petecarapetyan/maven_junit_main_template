package com.coderscampus;

public class Main {

  /**
   * PLEASE DO NOT PUT ANY CODE IN THIS CLASS FOR THIS ASSIGNMENT
   * 
   * To avoid confusing students with a static method that can only call static methods, 
   * this class only instantiates the App and, then launches that.
   * 
   * All the code which might otherwise go in this main method would then
   * go inside the App.launch() method
   * 
   * Static classes, methods, and fields are all wonderful things when you
   * know how to avoid the foot-guns associated with statics. But until
   * then, they are perhaps best avoided.
  */
  public static void main(String[] args){
    new App().launch();
  }
}
